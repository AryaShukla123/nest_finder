/**
 * Returns all properties. Currently synchronous mock data;
 * shaped so a real fetch (DB / API) can drop in later:
 *
 *   export async function getAllProperties(): Promise<Property[]> {
 *     const res = await fetch(`${process.env.API_URL}/properties`);
 *     return res.json();
 *   }
 */
export function getAllProperties(): Property[] {
  return ALL_PROPERTIES;
}

export function getFeaturedProperties(limit = 6): Property[] {
  return ALL_PROPERTIES.filter((p) => p.featured).slice(0, limit);
}

export function getCityCounts(): { city: string; count: number }[] {
  const counts = new Map<string, number>();
  for (const p of ALL_PROPERTIES) {
    counts.set(p.city, (counts.get(p.city) ?? 0) + 1);
  }
  return Array.from(counts, ([city, count]) => ({ city, count }));
}